public class EmailConfig {
    @Bean
    JavaMailSender getJavaMailSender() {
        final JavaMailSender mailSender = new JavaMailSenderImpl();
        ((JavaMailSenderImpl) mailSender).setHost("smtp.gmail.com");
        ((JavaMailSenderImpl) mailSender).setPort(25);

        ((JavaMailSenderImpl) mailSender).setUsername("alanwang53@gmail.com");
        ((JavaMailSenderImpl) mailSender).setPassword("435600Lu4");

        Properties props = ((JavaMailSenderImpl) mailSender).getJavaMailProperties();
        props.put("mail.transport.protocol", "smtp");
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.starttls.enable", "true");
        props.put("mail.debug", "true");
        return mailSender;
    }
}
